"""Ukázkový kód pro téma Cyklus for a while."""

for number in range(3):
    print('Opakování', number)

names = ['Eva', 'Adam']
for name in names:
    print('Jméno', name)

counter = 0
while counter < 3:
    print('While krok', counter)
    counter += 1


